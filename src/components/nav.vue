        v-if="isShow"
        class="nav-mobile-content"
        :class="{'enter-slideUp': enterSlideUp,'leave-slideDown': leaveSlideDown}"
      >
        <div class="list">
          <!-- <div v-for="(l, i) in list" :key="l.index" @click="handleClickMenu"
               class="item">
            <router-link to="/">首页</router-link>
          </div>-->
