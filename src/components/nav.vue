            <router-link to="/">
              <img class="logo" src="../assets/logo.jpg" alt="GolderBrother">
            </router-link>
          </el-col>
          <el-col :span="16">
            <el-menu
              :router="true"
              :default-active="activeIndex"
              active-text-color="#409eff"
              class="el-menu-demo"
              mode="horizontal"
              @select="handleSelect"
            >
              <el-menuItem
                :route="l.path"
                :index="l.index"
                v-for="l in list"
                :key="l.index"
              >{{l.name}}</el-menuItem>
            </el-menu>
          </el-col>
          <el-col v-if="userInfo._id" :span="5">
            <div class="nav-right">
              <el-dropdown @command="handleLogout">
                <span class="el-dropdown-link">
                  {{userInfo.name}}
                  <i class="el-icon-arrow-down el-icon--right"></i>
                </span>
                <img
                  v-if="!userInfo.avatar"
                  class="user-img"
                  src="../assets/user.png"
                  alt="GolderBrother"
                >
                <img
                  v-if="userInfo.avatar"
                  class="user-img"
                  :src="userInfo.avatar"
                  alt="GolderBrother"
                >
                <el-dropdown-menu slot="dropdown">
                  <el-dropdown-item command="logout">登 出</el-dropdown-item>
                </el-dropdown-menu>
              </el-dropdown>
            </div>
          </el-col>
          <el-col v-else :span="4">
            <div class="nav-right">
              <el-button size="small" type="primary" @click="handleClick('login')">登录</el-button>
              <el-button size="small" type="danger" @click="handleClick('register')">注册</el-button>
            </div>
          </el-col>
        </el-row>
      </div>
    </div>
