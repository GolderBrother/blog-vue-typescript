<script lang="ts">
import { Component, Vue, Watch } from "vue-property-decorator";
import { namespace } from "vuex-class";
import RegisterAndLogin from "@/components/registerAndLogin.vue";
import { isMobileOrPc, getQueryStringByName } from "@/utils/utils";
import { Route } from "vue-router";
import * as StorageUtils from "@/utils/storage";

const userModule = namespace("user");
@Component({
  components: {
    RegisterAndLogin
  }
})
export default class Nav extends Vue {
  // vuex-class
  @userModule.State(state => state.userInfo) myUserInfo;
  @userModule.Action("saveUser") saveUser; // 对应actions中的 saveUser
  @userModule.Action("clearUser") clearUser; // 对应actions中的 clearUser

  // loading: boolean = false;
  visible: boolean = false;
  handleFlag: string = "";
  title: string = "首页";
  list: Array<object> = [
    {
      index: "1",
      path: "/",
      name: "首页"
    },
    {
      index: "2",
      path: "/articles",
      name: "文章"
    },
    {
      index: "3",
      path: "/archive",
      name: "归档"
    },
    {
      index: "4",
      path: "/project",
      name: "项目"
    },
    {
      index: "5",
      path: "/timeline",
      name: "历程"
    },
    {
      index: "6",
      path: "/message",
      name: "留言"
    },
    {
      index: "7",
      path: "/about",
      name: "关于"
    }
  ];
  activeIndex: string = "1";
  enterSlideUp: boolean = false;
  leaveSlideDown: boolean = false;
  isShow: boolean = false;
  isMobile: boolean = isMobileOrPc();

  mounted() {
    // 授权登录的，有 code 参数
    this.routeChange(this.$route, this.$route);
    const code: string = getQueryStringByName("code");
    if (code) {
      this.getUser(code);
    }
  }

  get userInfo(): object {
    let userInfo: any = {
      _id: "",
      name: "",
      avatar: ""
    };
    // window.sessionStorage.userInfo
    if (StorageUtils.Session.get("userInfo")) {
      // userInfo = JSON.parse(window.sessionStorage.userInfo);
      userInfo = StorageUtils.Session.get("userInfo");
      // this.$store.commit("SAVE_USER", {
      //   userInfo
      // });
      this.saveUser(userInfo); // -> store.dispatch('saveUser', userInfo)
    }
    if (this.myUserInfo) {
      userInfo = this.myUserInfo; // this.myUserInfo -> this.$store.state.user.userInfo
    }
    // if (this.$store.state.user.userInfo) {
    //   userInfo = this.$store.state.user.userInfo;
    // }
    return userInfo;
  }

  @Watch("$route")
  routeChange(val: Route, oldVal: Route) {
    for (let i = 0; i < this.list.length; i++) {
      const l: any = this.list[i];
      if (l.path === val.path) {
        this.activeIndex = i + 1 + "";
        this.title = l.name;
        break;
      }
    }
  }

  handleClickMenu(route: string): void {
    this.isShow = false;
    if (route === "/login") {
      this.handleFlag = "login";
      this.visible = true;
    }
    if (route === "/register") {
      this.handleFlag = "register";
      this.visible = true;
    }
    if (route === "/logout") {
      this.handleLogout();
    }
  }
  handleMenu(): void {
    this.isShow = true;
    this.enterSlideUp = true;
  }
  handleHideMenu(): void {
    this.enterSlideUp = false;
    this.leaveSlideDown = true;
    setTimeout(() => {
      this.leaveSlideDown = false;
      this.isShow = false;
    }, 300);
  }

  // 获取用户信息
  async getUser(code: any): Promise<boolean> {
    const loading: any = this.$loading({
      lock: true,
      text: "Loading",
      spinner: "el-icon-loading",
      background: "rgba(255, 255, 255, 0.7)"
    });
    let res: any = await this.$https.post(
      this.$urls.getUser,
      { code },
      { withCredentials: true }
    );
    loading.close();
    if (res.status === 200) {
      if (res.data.code === 0) {
        const data: any = res.data.data;
        const userInfo: object = {
          _id: data._id,
          name: data.name,
          avatar: data.avatar
        };
        // this.$store.commit("SAVE_USER", {
        //   userInfo
        // });
        StorageUtils.Session.set("userInfo", userInfo);
        // 保存到vuex中
        this.saveUser(userInfo); // -> store.dispatch('saveUser', userInfo)
        // window.sessionStorage.userInfo = JSON.stringify(userInfo);
        this.$message({
          message: res.data.message,
          type: "success"
        });
        // let preventHistory = JSON.parse(window.sessionStorage.preventHistory);
        let preventHistory = StorageUtils.Session.get("preventHistory");
        if (preventHistory) {
          this.$router.push({
            path: preventHistory.name,
            query: preventHistory.query
          });
        }
        return false;
      } else {
        this.$message({
          message: res.data.message,
          type: "error"
        });
        return true;
      }
    } else {
      this.$message({
        message: "网络错误!",
        type: "error"
      });
      return true;
    }
  }

  // 登出
  handleLogout(): void {
    // window.sessionStorage.userInfo = "";
    StorageUtils.Session.set("userInfo", "");
    // this.$store.commit("SAVE_USER", {
    //   _id: "",
    //   name: "",
    //   avatar: ""
    // });
    this.clearUser(); // -> store.dispatch('clearUser', userInfo)
  }

  handleClick(value: string): void {
    this.handleFlag = value;
    this.visible = true;
  }

  handleCancel(value: boolean): void {
    this.visible = value;
  }
  handleOk(value: boolean): void {
    this.visible = value;
  }
  handleSelect(val: string, oldVal: string): void {
    // console.log("val :", val);
    // console.log("oldVal :", oldVal);
    this.activeIndex = val;
  }
}
</script>

